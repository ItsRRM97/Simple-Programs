package Patterns_Programs;

// to print the pattern as in output
class Pattern4
{
    public static void main()
    {int i; int j;
        for(i=1;i<=5;i++)
        {
            for(j=1;j<=i;j++)
            {
                System.out.print("*");
            }
        System.out.println();
    }
    }
}
